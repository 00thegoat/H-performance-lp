// Generates from public/replica.html:
//   src/snapshot/styles.css     — all <style> blocks concatenated
//   src/snapshot/body.ts        — the entire body HTML as a default-exported string
//
// Why one body file instead of per-section files: the Wix DOM is a deeply nested
// single container (#SITE_CONTAINER). Splitting at our anchor markers cuts mid-tree,
// breaking layout. Keeping it as one tree preserves visual fidelity. Editing is
// still done via content.json → `npm run patch`.

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";

const SRC = "public/replica.html";
const OUT_DIR = "src/snapshot";
mkdirSync(OUT_DIR, { recursive: true });

const html = readFileSync(SRC, "utf8");
console.log(`loaded ${SRC} (${(html.length / 1024).toFixed(0)} KB)`);

// 1) Extract <style> blocks → styles.css
const styleBlocks = [];
const htmlNoStyles = html.replace(
  /<style\b[^>]*>([\s\S]*?)<\/style>/gi,
  (_, body) => {
    styleBlocks.push(body);
    return "";
  }
);
writeFileSync(`${OUT_DIR}/styles.css`, styleBlocks.join("\n\n"));
console.log(`✓ ${styleBlocks.length} style blocks → ${OUT_DIR}/styles.css`);

// 2) Extract body content (file has no closing </body> tag)
const bodyOpen = htmlNoStyles.search(/<body\b[^>]*>/i);
if (bodyOpen < 0) {
  console.error("✗ <body> not found");
  process.exit(1);
}
const afterOpen = htmlNoStyles.slice(bodyOpen).replace(/^<body\b[^>]*>/i, "");
const closeIdx = afterOpen.search(/<\/body>/i);
const body = closeIdx >= 0 ? afterOpen.slice(0, closeIdx) : afterOpen;

// 3) Strip <script> tags (DOM already rendered, scripts not needed)
const stripScripts = (s) =>
  s.replace(/<script\b[^>]*>[\s\S]*?<\/script>/gi, "");

// 4) Strip HTML attributes that confuse React (HTML parsers lowercase attribute
//    names per spec, so renaming to camelCase in the string doesn't help — the
//    parser normalizes them back. These are all optional perf/UA hints, safe to drop.)
const ATTRS_TO_STRIP = [
  "fetchpriority",
  "fetchPriority",
  "crossorigin",
  "referrerpolicy",
  "data-ssr-src-done",
  "data-has-ssr-src",
  "data-bbox",
  "data-image-info",
  "data-bg-effect-name",
  "data-motion-part",
  "data-testid",
];
const fixReactAttrs = (s) => {
  for (const attr of ATTRS_TO_STRIP) {
    // attr="value" (quoted)
    s = s.replace(new RegExp(`\\s+${attr}="[^"]*"`, "g"), "");
    // attr='value' (single quoted)
    s = s.replace(new RegExp(`\\s+${attr}='[^']*'`, "g"), "");
    // attr=value (unquoted)
    s = s.replace(new RegExp(`\\s+${attr}=[^\\s>]+`, "g"), "");
    // bare attribute (no value)
    s = s.replace(new RegExp(`\\s+${attr}\\b(?=[\\s>])`, "g"), "");
  }
  // Strip stray bare attributes htmlparser2 chokes on
  s = s.replace(/\s+srcSet(?=[\s>])/g, ""); // bare srcSet without value
  s = s.replace(/\s+sizes(?=[\s>])/g, ""); // bare sizes without value
  return s;
};

// 5) Write body as a default-exported template literal string
const cleaned = fixReactAttrs(stripScripts(body));
const escaped = cleaned.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
const ts = `// Auto-generated by scripts/split-snapshot.mjs — do not edit by hand.
// Run \`npm run patch\` to regenerate after editing content.json.
const html: string = \`${escaped}\`;
export default html;
`;
writeFileSync(`${OUT_DIR}/body.ts`, ts);
console.log(`✓ body: ${(cleaned.length / 1024).toFixed(1)} KB`);
console.log("\n✅ split done.");
